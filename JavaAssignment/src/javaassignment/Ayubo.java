    private void CalculateHIreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateHIreActionPerformed
        Double total = 0.0;
        if(Package100km.isSelected()){
            total += package_100;
        }
        else if(Package200km.isSelected()){
            total += package_200;
        }


        total += extra_km * Integer.parseInt(ExtraKm.getText());

        TotalHire.setText(total.toString());
    }//GEN-LAST:event_CalculateHIreActionPerformed

