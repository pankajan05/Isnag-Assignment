    private void CalculateRentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalculateRentActionPerformed
        int days = Integer.parseInt(DatesInput.getText());

        Double total = this.weekly_rent * (int) (days/7) + this.daily_rent * (int)(days%7) + days * this.daliy_driver_cost;
        TotalRent.setText(total.toString());
    }//GEN-LAST:event_CalculateRentActionPerformed

